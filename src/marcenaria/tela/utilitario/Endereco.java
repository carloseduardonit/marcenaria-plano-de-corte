    private void txtfCEPKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtfCEPKeyPressed
        if (txtfCEP.getText().length() > 7) {
            CEP.ObterEnderecodeCEP(txtfCEP.getText());
            txtEndereco.setText(marcenaria.pessoa.utilitario.Endereco.getTipoLogradouro() + " " + marcenaria.pessoa.utilitario.Endereco.getLogradouro());
            txtComplemento.setText(marcenaria.pessoa.utilitario.Endereco.getComplemento());
            txtBairro.setText(marcenaria.pessoa.utilitario.Endereco.getBairro());
            txtCidade.setText(marcenaria.pessoa.utilitario.Endereco.getCidade());
            txtEstado.setText(marcenaria.pessoa.utilitario.Endereco.getEstado());
        }
    }//GEN-LAST:event_txtfCEPKeyPressed
    private void setarCampos(JTextField Login, JTextField CEP, JTextField endereco, JTextField numero, JTextField complemento, JTextField bairro, JTextField cidade, JTextField estado) {
        Login.setText(marcenaria.pessoa.utilitario.Endereco.getCep());//
        CEP.setText(marcenaria.pessoa.utilitario.Endereco.getCep());
        endereco.setText(marcenaria.pessoa.utilitario.Endereco.getTipoLogradouro() + " " + marcenaria.pessoa.utilitario.Endereco.getLogradouro());
        numero.setText(String.valueOf(marcenaria.pessoa.utilitario.Endereco.getNumero()));
        complemento.setText(marcenaria.pessoa.utilitario.Endereco.getComplemento());
        bairro.setText(marcenaria.pessoa.utilitario.Endereco.getBairro());
        cidade.setText(marcenaria.pessoa.utilitario.Endereco.getCidade());
        estado.setText(marcenaria.pessoa.utilitario.Endereco.getEstado());
    }

